    <h2>ICHEP 2016</h2>
    <p>
      <b>Integration of ROOT Notebooks as a web-based ATLAS analysis tool for public data releases and outreach</b>
      Integration of the ROOT data analysis framework with the Jupyter Notebook technology presents the potential of enhancement and expansion of educational and training programs. It can be beneficial for university students in their early years, new PhD students and post doctoral researchers, as well as for senior researchers and professors who want to restart their contact with the analysis of data or to introduce a more friendly and yet very powerful open source tool in the classroom. Such tools have been already tested in several environments. A fully web-based integration of the tools and the Open Access Data repositories brings the possibility to go a step forward in the ATLAS quest of making use of several CERN projects in the field of the education and training, developing new computing solutions on the way.
    </p>

